public class Exam03 {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / (double)b);
		System.out.println(a % b); // % = 나머지(몫)를 구하는 연산자
	}

}
